# 숫자처리 함수
# Python에서 기초적으로 제공하는 함수 
print(abs(-5)) # 5
print(pow(4, 2)) # 4^2 = 4 * 4 = 16
print(max(5, 12)) # 최대값을 반환하기
print(min(5, 12)) # 최소값을 반환하기 
print(round(3.14)) # 반올림

from math import * # math 라이브러리 모든 것을 사용하겠다 
print(floor(2.786)) # 2, 내림 작업
print(ceil(2.786)) # 3, 올림 작업 
print(sqrt(25)) # 제곱근 구하는 함수 